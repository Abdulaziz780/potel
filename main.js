// Запрашиваем число у пользователя
const number = prompt("Введите число:");

// Получаем текущий день недели (0 - воскресенье, 1 - понедельник, ..., 6 - суббота)
const today = new Date().getDay();

// Проверяем, является ли сегодня будним днем (понедельник - пятница)
const isWeekday = today >= 1 && today <= 5;

// Если введенное число и сегодня будний день, показываем его в alert
if (!isNaN(number) && isWeekday) {
  alert("Сегодня будний день, введенное число: " + number);
} else {
  alert("Сегодня выходной или введено некорректное число.");
}
